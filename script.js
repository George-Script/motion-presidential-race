function openModal(key) {
  var d = modalData[key];
  if (!d) return;
  document.getElementById("modalHeader").innerHTML =
    '<button class="modal-close" onclick="closeModal()">&#x2715;</button>' +
    '<div class="modal-header-letter">' +
    d.letter +
    "</div>" +
    '<div class="modal-header-tag">' +
    d.tag +
    "</div>" +
    '<div class="modal-header-title">' +
    d.title +
    "</div>";
  var body =
    '<div class="modal-section"><div class="modal-section-title">Overview</div><p class="modal-text">' +
    d.overview +
    "</p></div>" +
    '<div class="modal-section"><div class="modal-section-title">What This Means in Practice</div><ul class="modal-list">' +
    d.details
      .map(function (i) {
        return "<li>" + i + "</li>";
      })
      .join("") +
    "</ul></div>" +
    '<div class="modal-section"><div class="modal-section-title">Why It Matters</div><p class="modal-text">' +
    d.why +
    "</p></div>" +
    '<div class="modal-section"><div class="modal-section-title">Implementation Timeline</div><div class="timeline">' +
    d.timeline
      .map(function (t) {
        return (
          '<div class="timeline-item"><div class="timeline-phase">' +
          t.phase +
          '</div><div class="timeline-event">' +
          t.event +
          '</div><div class="timeline-desc">' +
          t.desc +
          "</div></div>"
        );
      })
      .join("") +
    "</div></div>";
  document.getElementById("modalBody").innerHTML = body;
  document.getElementById("modalOverlay").classList.add("active");
  document.body.style.overflow = "hidden";
}
function closeModal() {
  document.getElementById("modalOverlay").classList.remove("active");
  document.body.style.overflow = "";
}
document.addEventListener("keydown", function (e) {
  if (e.key === "Escape") closeModal();
});

function submitNewsletter() {
  var name = document.getElementById("nlFirstName").value.trim();
  var email = document.getElementById("nlEmail").value.trim();
  if (!name || !email) {
    alert("Please fill in at least your first name and email to join.");
    return;
  }
  document.getElementById("nlFormContainer").style.display = "none";
  document.getElementById("nlSuccess").style.display = "block";
}
